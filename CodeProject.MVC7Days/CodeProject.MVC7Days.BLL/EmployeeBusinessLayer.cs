using System.Collections.Generic;
using System.Linq;
using CodeProject.MVC7Days.DAL;
using CodeProject.MVC7Days.Models;

namespace CodeProject.MVC7Days.BLL
{
    public class EmployeeBusinessLayer
    {
        public UserStatus GetUserValidity(UserDetails u)
        {
            if (u.UserName == "Admin" && u.Password == "Admin")
            {
                return UserStatus.AuthenticatedAdmin;
            }
            else if (u.UserName == "Sukesh" && u.Password == "Sukesh")
            {
                return UserStatus.AuthentucatedUser;
            }
            else
            {
                return UserStatus.NonAuthenticatedUser;
            }
        }

        public List<Employee> GetEmployees()
        {
            SalesERPDAL salesDal = new SalesERPDAL();
            return salesDal.Employees.ToList();
        }

        public Employee GetEmployeeById(int pId)
        {
            SalesERPDAL salesDal = new SalesERPDAL();
            return salesDal.Employees.Where(employee => employee.EmployeeId == pId).ToList().FirstOrDefault();
        }

        public Employee SaveEmployee(Employee e)
        {
            SalesERPDAL salesDal = new SalesERPDAL();
            salesDal.Employees.Add(e);
            salesDal.SaveChanges();
            return e;
        }

        public void UploadEmployees(List<Employee> employees)
        {
            SalesERPDAL salesDal = new SalesERPDAL();
            salesDal.Employees.AddRange(employees);
            salesDal.SaveChanges();
        }
    }
}